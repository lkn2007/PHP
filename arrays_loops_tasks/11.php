# Виведіть стовпец парних чисел в проміжку від 0 до 100.

for ($i = 0; $i <= 100; $i++) {
    if ($i % 2 === 0) {
        echo $i . "\n";
    }
}

for ($i = 0; $i <= 100; $i++) {
    echo ($i % 2 === 0) ? $i . "\n" : "";
}

$i = 0;
while ($i <= 100) {
    echo ($i % 2 === 0) ? $i . "\n" : "";
    $i++;
}
