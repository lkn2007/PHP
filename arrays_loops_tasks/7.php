# Дано масив з єлементами 2, 5, 9, 15, 0, 4.
# За допомогою цикла foreach  і оператора if виведіть на єкран
# стовпець тих єлементів масива, які більше 3, але меньше 10.

$arr = array(2, 5, 9, 15, 0, 4);

foreach ($arr as $value) {
    if ($value > 3 && $value < 10) {
        echo $value . "\n";
    }
}
