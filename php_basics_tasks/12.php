# За допомогою конструкції switch виведіть фразу: "Невідомий день", якщо значення
# зміної day не попадає в діапазон чисел від 1 до 7 (включно).

$day = 0;
switch ($day) {
    case ($day >= 1 && $day <= 5):
        echo 'Це робочий день';
        break;
    case ($day >= 6 && $day <= 7):
        echo 'Це вихідний день';
    default:
        echo 'Невідомий день';
}
