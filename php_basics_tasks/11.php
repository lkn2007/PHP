# За допомогою конструкції switch виведіть фразу: "Це вихідний день", якщо значення
# зміної day попадає в діапазон чисел від 6 до 7 (включно).

$day = 0;
switch ($day) {
    case ($day >= 1 && $day <= 5):
        echo 'Це робочий день';
        break;
    case ($day >= 6 && $day <= 7):
        echo 'Це вихідний день';
}
